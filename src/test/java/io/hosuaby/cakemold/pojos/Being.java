package io.hosuaby.cakemold.pojos;

/**
 * Example of abstract class.
 *
 * @author Alexei KLENIN
 */
public abstract class Being {

  private int members;

  public Being() {
  }

  public int getMembers() {
    return members;
  }

  public void setMembers(int members) {
    this.members = members;
  }
}
